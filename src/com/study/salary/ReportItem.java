package com.study.salary;

public class ReportItem {

    private int year;
    private double startingSalary;
    private int numberOfIncrements;
    private double increment;
    private double incrementAmount;
    private int numberOfDeduction;
    private double deduction;
    private double deductionAmount;

    public int getYear() {
        return year;
    }

    public void setYear(int year) {
        this.year = year;
    }

    public double getStartingSalary() {
        return startingSalary;
    }

    public void setStartingSalary(double startingSalary) {
        this.startingSalary = startingSalary;
    }

    public int getNumberOfIncrements() {
        return numberOfIncrements;
    }

    public void setNumberOfIncrements(int numberOfIncrements) {
        this.numberOfIncrements = numberOfIncrements;
    }

    public double getIncrement() {
        return increment;
    }

    public void setIncrement(double increment) {
        this.increment = increment;
    }

    public double getIncrementAmount() {
        return incrementAmount;
    }

    public void setIncrementAmount(double incrementAmount) {
        this.incrementAmount = incrementAmount;
    }

    public int getNumberOfDeduction() {
        return numberOfDeduction;
    }

    public void setNumberOfDeduction(int numberOfDeduction) {
        this.numberOfDeduction = numberOfDeduction;
    }

    public double getDeduction() {
        return deduction;
    }

    public void setDeduction(double deduction) {
        this.deduction = deduction;
    }

    public double getDeductionAmount() {
        return deductionAmount;
    }

    public void setDeductionAmount(double deductionAmount) {
        this.deductionAmount = deductionAmount;
    }
}
